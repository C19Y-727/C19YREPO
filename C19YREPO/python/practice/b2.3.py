# 定义变量名后在打印里面加f引用变量名，同时变量名加花括号{}
name="Eric"
print(f"hello {name},would you like to learn some Python today?")
print()

# lower为全小写
name="Eric".lower()
print(f"hello {name},would you like to learn some Python today?")
print()

# upper为全大写
name="Eric".upper()
print(f"hello {name},would you like to learn some Python today?")
print()

# title为首字母大写
name="Eric chen".title()
print(f"hello {name},would you like to learn some Python today?")

